#!/usr/bin/env node
// Bake the per-class aircraft sample glbs into a plain-JS geometry module so the MapLibre
// custom WebGL layer can draw them WITHOUT a runtime glTF loader / three.js. Extracts POSITION,
// NORMAL, COLOR_0 and indices from each glb's single mesh primitive.
// Run: node scripts/build-aircraft-geometry.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const SAMPLES = path.join(HERE, "../web/public/aircraft-samples");
const OUT = path.join(HERE, "../web/src/aircraft-geometry.js");

const COMPONENT = {
  5120: { array: Int8Array, size: 1 },
  5121: { array: Uint8Array, size: 1 },
  5122: { array: Int16Array, size: 2 },
  5123: { array: Uint16Array, size: 2 },
  5125: { array: Uint32Array, size: 4 },
  5126: { array: Float32Array, size: 4 },
};
const TYPE_COMPONENTS = { SCALAR: 1, VEC2: 2, VEC3: 3, VEC4: 4 };

function parseGlb(buf) {
  if (buf.readUInt32LE(0) !== 0x46546c67) throw new Error("not a glb");
  const jsonLen = buf.readUInt32LE(12);
  const json = JSON.parse(buf.slice(20, 20 + jsonLen).toString("utf8"));
  // BIN chunk follows the JSON chunk (8-byte header each).
  let offset = 20 + jsonLen;
  let bin = null;
  while (offset < buf.length) {
    const chunkLen = buf.readUInt32LE(offset);
    const chunkType = buf.readUInt32LE(offset + 4);
    const data = buf.slice(offset + 8, offset + 8 + chunkLen);
    if (chunkType === 0x004e4942) bin = data; // "BIN\0"
    offset += 8 + chunkLen;
  }
  return { json, bin };
}

function readAccessor(json, bin, index) {
  const acc = json.accessors[index];
  const view = json.bufferViews[acc.bufferView];
  const comp = COMPONENT[acc.componentType];
  const numComps = TYPE_COMPONENTS[acc.type];
  const count = acc.count * numComps;
  const byteOffset = (view.byteOffset || 0) + (acc.byteOffset || 0);
  // bufferViews here are tightly packed (no byteStride) for these baked models.
  const src = new comp.array(bin.buffer, bin.byteOffset + byteOffset, count);
  let out = Array.from(src);
  if (acc.normalized) {
    const max = acc.componentType === 5121 ? 255 : acc.componentType === 5123 ? 65535 : acc.componentType === 5120 ? 127 : 32767;
    out = out.map((v) => (acc.componentType === 5120 || acc.componentType === 5122 ? Math.max(v / max, -1) : v / max));
  }
  return out;
}

function round(arr, dp) {
  const f = 10 ** dp;
  return arr.map((v) => Math.round(v * f) / f);
}

const classes = {};
for (const cls of ["small", "medium", "large"]) {
  const buf = fs.readFileSync(path.join(SAMPLES, `${cls}.glb`));
  const { json, bin } = parseGlb(buf);
  const prim = json.meshes[0].primitives[0];
  const positions = readAccessor(json, bin, prim.attributes.POSITION);
  const normals = readAccessor(json, bin, prim.attributes.NORMAL);
  const colors = prim.attributes.COLOR_0 != null ? readAccessor(json, bin, prim.attributes.COLOR_0) : null;
  const indices = readAccessor(json, bin, prim.indices);
  const mat = json.materials?.[prim.material]?.pbrMetallicRoughness || {};
  classes[cls] = {
    positions: round(positions, 5),
    normals: round(normals, 5),
    colors: colors ? round(colors, 4) : null,
    indices,
    vertexCount: positions.length / 3,
    baseColor: mat.baseColorFactor || [1, 1, 1, 1],
    metallic: mat.metallicFactor ?? 1,
    roughness: mat.roughnessFactor ?? 1,
  };
  console.log(`${cls}: ${classes[cls].vertexCount} verts, ${indices.length} indices, colors=${!!colors}`);
}

const body = `// GENERATED by scripts/build-aircraft-geometry.mjs — do not edit by hand.
// Per-class aircraft geometry baked from web/public/aircraft-samples/{small,medium,large}.glb.
// Model frame: +X nose (forward), +Y wing (right/left span), +Z up. Units are model units.
export const AIRCRAFT_GEOMETRY = ${JSON.stringify(classes)};
`;
fs.writeFileSync(OUT, body);
console.log(`wrote ${OUT} (${(body.length / 1024).toFixed(1)} KB)`);
